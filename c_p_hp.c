/*******************************************************************************

a 1 1 A
1 a a 1
1 a a 1
a 1 1 A

*********************************************************************************/

#include <stdio.h>

int main()
{   int n = 6, m=8;
    char arr[n][m];
    int i, j;
    
    for(i=0; i<n; i++){
        for(j=0; j<m; j++){
            if((i==0 && j==0) || (i==(n-1) && j==0)){
                arr[i][j]= 'a';
            }else if((i==0 && j==(m-1)) || (i==(n-1) && j==(m-1))){
                arr[i][j]= 'A';
            }else if(i==0 || i==(n-1) || j==0 || j==(m-1)){
                arr[i][j]= '1';
            }else{
                arr[i][j]= 'a';
            }
                printf("%c\t", arr[i][j]);
        }
        printf("\n");
    }
    

    return 0;
}
